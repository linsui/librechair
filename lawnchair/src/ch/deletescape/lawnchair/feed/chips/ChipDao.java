package ch.deletescape.lawnchair.feed.chips;

import androidx.room.Dao;
import androidx.room.Delete;
import androidx.room.Insert;
import androidx.room.Query;

import java.util.List;

@Dao
public abstract class ChipDao {
    @Query("select * from chipprovidercontainer order by `order`")
    public abstract List<ChipProviderContainer> getAll();

    @Insert
    public abstract void add(ChipProviderContainer chip);

    @Delete
    public abstract void remove(ChipProviderContainer chip);

    @Query("delete from chipprovidercontainer")
    public abstract void removeAll();
}
